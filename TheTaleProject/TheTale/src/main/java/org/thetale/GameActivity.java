    public enum FRAGMENTS {
        DIARY(DiaryFragment.class, R.string.title_section1), JOURNAL(JournalFragment.class, R.string.title_section2), BLANK(DummySectionFragment.class, R.string.title_section3);

        private final Constructor<? extends Fragment> myConstructor;
        private final int myTitleID;

        FRAGMENTS(Class<? extends Fragment> clazz, final int titleID) {
            myTitleID = titleID;
            try {
                myConstructor = clazz.getConstructor(Context.class, RequestManager.class);
            } catch (Throwable t) {
                throw new RuntimeException(t);
            }
            assert myConstructor != null;
        }

        Fragment createMe(Context context, RequestManager requestManager) {
            try {
                return myConstructor.newInstance(context, requestManager);
            } catch (Throwable t) {
                throw new RuntimeException(t);
            }
        }

        public int getMyTitleID() {
            return myTitleID;
        }

        public static FRAGMENTS get(int position) {
            if (position >= FRAGMENTS.values().length) {
                return BLANK;
            }
            return FRAGMENTS.values()[position];
        }

        public static int getCount() {
            return FRAGMENTS.values().length - 1;
        }
    }

    /**
     * A {@link android.support.v4.app.FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            Fragment fragment = FRAGMENTS.get(position).createMe(GameActivity.this, myRequestManager);
            Bundle args = new Bundle();
            args.putInt(DummySectionFragment.ARG_SECTION_NUMBER, position + 1);
            fragment.setArguments(args);
            return fragment;
        }

        @Override
        public int getCount() {
            return FRAGMENTS.getCount();
        }

        @Override
        public CharSequence getPageTitle(int position) {
            return getString(FRAGMENTS.get(position).getMyTitleID()).toUpperCase(Locale.getDefault());
        }
    }

